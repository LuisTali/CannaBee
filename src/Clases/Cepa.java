package Clases;

public class Cepa {
    private String nombre;
    private String raza;
    private double thc;
    private String comentarios;

    public Cepa(String nombre, String raza, double thc, String comentarios) {
        this.nombre = nombre;
        this.raza = raza;
        this.thc = thc;
        this.comentarios = comentarios;
    }
}
